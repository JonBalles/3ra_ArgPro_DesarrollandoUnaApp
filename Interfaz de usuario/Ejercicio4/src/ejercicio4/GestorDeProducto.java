     //Boton busqueda
    private void jbGestBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbGestBuscarActionPerformed
       boolean esta = false;
        
        try
        {
            for(Producto prod : DeTodoSA.listaProductos){
            
            if(prod.getCodigo() == Integer.parseInt(jtGestCodigo.getText())){
                esta= true;
                
                jtGestDescripcion.setText(prod.getDescripcion());
                jtGestPrecio.setText(prod.getPrecio()+ "");
                jtGestStock.setText(prod.getStock()+"");
                jcGestCategoria.setSelectedItem(prod.getRubro());
                return;
        }
            }
        } catch(NumberFormatException err){
        JOptionPane.showMessageDialog(null, "Solo caracteres numericos");
        //La validacion siguiente es para que no tire el mensaje de que no se encuentra el producto
        esta=true;
        }
        
        if(!esta){
        JOptionPane.showMessageDialog(null, "No se encuentra el producto");
         jtGestDescripcion.setText("");
         jtGestPrecio.setText("");
         jtGestStock.setText("");
        }
    }//GEN-LAST:event_jbGestBuscarActionPerformed

