private void cargarCombo(){
 jcGestCategoria.removeAllItems();
    
    
    for(Categorias cat : Categorias.values()){
        jcGestCategoria.addItem(cat);
    }
}

private void nuevoProducto(){
   try {
    
     // Creo las variables que extraen los datos de mis jtGest...
    int cod = DeTodoSA.listaProductos.last().getCodigo() + 1;
    String desc = jtGestDescripcion.getText();
    double price = Double.parseDouble(jtGestPrecio.getText());
    int stock = Integer.parseInt(jtGestStock.getText());
    Categorias rubro =  (Categorias)  jcGestCategoria.getSelectedItem();
    
    
    // Constructor de Producto - int codigo, String descripcion, double precio, int stock, Categorias rubro
    Producto addProduct = new Producto(cod, desc, price, stock, rubro);
    
    //Agrego el producto a mi lista de productos
    DeTodoSA.listaProductos.add(addProduct);
    
    //Doy un mensaje de producto creado
    JOptionPane.showMessageDialog(null, "Se ha creado un producto con exito");
    
   }
   catch(NumberFormatException e){
       jtGestDescripcion.setText("");
       jtGestPrecio.setText("");
       jtGestStock.setText("");
       jtGestCodigo.setText("");
       JOptionPane.showMessageDialog(null, "Hubo un error en el ingreso de datos. Por favor, intente nuevamente");
   }
}

private void editProducto(){
     try {
    
     // Creo las variables que extraen los datos de mis jtGest...
    int cod = Integer.parseInt(jtGestCodigo.getText());
    String desc = jtGestDescripcion.getText();
    double price = Double.parseDouble(jtGestPrecio.getText());
    int stock = Integer.parseInt(jtGestStock.getText());
    Categorias rubro =  (Categorias)  jcGestCategoria.getSelectedItem();
    
    //Hay que eliminar el Producto para volver a instanciarlo nuevamente editado
    
    Producto productDestroy = null;
    //Lo buscamos por codigo
    for(Producto prod : DeTodoSA.listaProductos){
        if(cod == prod.getCodigo()){
            productDestroy = prod;
            break;
        }
    }
    
    if(productDestroy != null){
        //Elimino el producto
       DeTodoSA.listaProductos.remove(productDestroy);
        
     // Constructor de Producto - int codigo, String descripcion, double precio, int stock, Categorias rubro
    Producto addProduct = new Producto(cod, desc, price, stock, rubro);
    
    //Agrego el producto a mi lista de productos
    DeTodoSA.listaProductos.add(addProduct);
    
    //Doy un mensaje de producto creado
    JOptionPane.showMessageDialog(null, "Se ha editado un producto con exito");
    }
    
    
  
    
   }
   catch(NumberFormatException e){
       jtGestDescripcion.setText("");
       jtGestPrecio.setText("");
       jtGestStock.setText("");
       jtGestCodigo.setText("");
       JOptionPane.showMessageDialog(null, "Hubo un error en el ingreso de datos. Por favor, intente nuevamente");
   }
    
}

